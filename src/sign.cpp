//------------------------------------------------------------------------------
// double sign(double a)
//------------------------------------------------------------------------------
/**
 * Returns the sign of a number
 *
 * @param <a> double
 *
 * @return 1.0 if a > 0, -1.0 if a < 0 and 0 if a == 0
 */
//------------------------------------------------------------------------------
double sign(double a) {
    if(a != 0)
        return a/fabs(a);
    return 0;
}

